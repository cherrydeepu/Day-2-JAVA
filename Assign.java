import java.util.*;
public class Assign{
    public static void main(String args[]){
        Scanner S=new Scanner(System.in);
        int a,b;
        a=S.nextInt();
        b=S.nextInt();
        System.out.println(a<b);
        System.out.println(a>b);
        System.out.println(a<=b);
        System.out.println(a>=b);
        System.out.println(a==b);
        System.out.println(a!=b);
    }
}